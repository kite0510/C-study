#include <stdio.h>

int main(void) {
    // 예제 11-1.c (대문자를 소문자로 변경)
    // -------------------------------------------------------
    char small, cap = 'G'; // char형 변수 선언과 초기화

    if ((cap >= 'A') && (cap <= 'Z')) { // cap이 대문자인지 확인
        small = cap + ('a' - 'A'); // 대/소문자 간의 차이를 더해 소문자로 변환
    }

    printf("대문자 : %c %c\n", cap, '\n');    // '\n' 출력하면 줄 바뀜.
    printf("소문자 : %c\n", small);

    // 예제 11-2.c (공백이나 제어 문자의 입력)
    // -------------------------------------------------------
    char ch1, ch2;

    scanf("%c%c", &ch1, &ch2); // 2개의 문자를 연속 입력 받음
    printf("[%c%c]", ch1, ch2); // 입력된 문자 출력 (대괄호로 묶어서 확인)
    // 주의: scanf("%c%c")는 공백, 탭, 개행 문자도 입력으로 받습니다.
    //       만약 공백 없이 두 문자를 입력받으려면, scanf("%c %c", &ch1, &ch2); 처럼
    //       형식 문자열 사이에 공백을 넣어야 합니다.
    printf("\n");


    // 예제 11-3.c (getchar 함수와 putchar 함수 사용)
    // -------------------------------------------------------
    int ch; // getchar()의 반환형은 int (EOF 처리를 위해)

    ch = getchar();         // getchar() 함수를 사용하여 문자 하나를 입력받음
    printf("입력한 문자 : ");
    putchar(ch);          // putchar() 함수를 사용하여 입력받은 문자 출력
    putchar('\n');        // 개행 문자 출력
    return 0;
}
